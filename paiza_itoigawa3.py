# randomクラスを読み込む
import random

# 結果出力関数
def result(msg):
  print("僕の今日のおかずは" + msg + "です")

# データ配列
array1 = ["秋元真夏","長濱ねる","矢久保美緒","与田祐希","影山優佳","遠藤さくら","齋藤飛鳥"]

# 乱数を使ってランダムに配列から要素を取り出す
num = random.randint(0,len(array1)-1)
c1 = array1[num]
# 出力
result(c1)

