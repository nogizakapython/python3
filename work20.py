# mathライブラリを使った最小公倍数、sin,tanの値
import math
from math import pi as PI

# 最小公倍数
a = math.lcm(3,5)
print(a)

print(math.sin(PI))
print(math.sin(PI/2))
print(math.sin(3 * PI/2))
print(math.sin(math.radians(90)))
print(math.sin(math.radians(30)))
print(math.sin(math.radians(15)))

print(math.tan(PI))
print(math.tan(PI/3))
print(math.tan(PI/4))
print(math.tan(math.radians(30)))
print(math.tan(math.radians(15)))
print(math.tan(math.radians(60)))
print(math.tan(math.radians(75)))

